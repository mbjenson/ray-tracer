#include <string>

/**
 * Checks if a string is a floating point number.
 * This is done by checking each character in the string.
 * @param str the string to be evaluated.
 * @return 1 if str is a float, 0 if not a float.
*/
int isFloat(std::string str);

/**
 * Checks if a string is an integer.
 * Done by checking each character in a given string.
 * @param str the string to be evaluated.
 * @return 1 if str is an int, 0 if not an int.
*/
int isInt(std::string str);